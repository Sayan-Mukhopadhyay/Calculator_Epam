package epam;

public class Calculator {
    public void add(float num1, float num2){
        System.out.println(num1 + num2);
    }
    public void sub(float num1 , float num2){
        System.out.println(num1-num2);
    }
    public void mul(float num1 , float num2){
        System.out.println(num1 * num2);
    }
    public void div(float num1 , float num2){
        System.out.println(num1/num2);
    }
}
